package com.bluemetrix.friends;

import java.util.Comparator;

//sort by friend Rank
public class FriendSortByRank implements Comparator 
{
	public int compare(Object o1, Object o2)
	{
		Friend p1 = (Friend) o1;
	    Friend p2 = (Friend) o2; 
	    return p1.getFriendRank() - p2.getFriendRank();
	}
}